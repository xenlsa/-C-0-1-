#include <iostream>
using namespace std;
int main ()
{
    //转义字符
    //换行符 \n
    cout << "hello \nworld" << endl;

    //反斜线
    cout << "\\" << endl;

    //水平制表符\t  作用:整齐的输出数据
    cout << "aaa"<< "\t" << "bbb"<<endl;
    cout << "aa"<< "\t" << "bb"<<endl;


    
    system("pause");
    return 0;
}